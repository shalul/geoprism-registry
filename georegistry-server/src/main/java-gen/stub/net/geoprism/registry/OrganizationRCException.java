package net.geoprism.registry;

public class OrganizationRCException extends OrganizationRCExceptionBase
{
  private static final long serialVersionUID = 1763126359;
  
  public OrganizationRCException()
  {
    super();
  }
  
  public OrganizationRCException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public OrganizationRCException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public OrganizationRCException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
