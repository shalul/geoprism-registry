package net.geoprism.registry.etl.export.dhis2;

public class NoParentException extends NoParentExceptionBase
{
  private static final long serialVersionUID = 68388092;
  
  public NoParentException()
  {
    super();
  }
  
  public NoParentException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public NoParentException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public NoParentException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
