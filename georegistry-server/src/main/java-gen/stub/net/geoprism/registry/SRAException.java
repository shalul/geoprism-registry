package net.geoprism.registry;

public class SRAException extends SRAExceptionBase
{
  private static final long serialVersionUID = -968026980;
  
  public SRAException()
  {
    super();
  }
  
  public SRAException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public SRAException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public SRAException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
