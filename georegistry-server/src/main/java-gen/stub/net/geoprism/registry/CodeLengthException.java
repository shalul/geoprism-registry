package net.geoprism.registry;

public class CodeLengthException extends CodeLengthExceptionBase
{
  private static final long serialVersionUID = 1130469806;
  
  public CodeLengthException()
  {
    super();
  }
  
  public CodeLengthException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public CodeLengthException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public CodeLengthException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
