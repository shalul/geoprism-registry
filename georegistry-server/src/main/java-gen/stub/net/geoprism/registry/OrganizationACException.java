package net.geoprism.registry;

public class OrganizationACException extends OrganizationACExceptionBase
{
  private static final long serialVersionUID = 1154803142;
  
  public OrganizationACException()
  {
    super();
  }
  
  public OrganizationACException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public OrganizationACException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public OrganizationACException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
