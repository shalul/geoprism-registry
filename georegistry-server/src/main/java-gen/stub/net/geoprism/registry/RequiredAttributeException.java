package net.geoprism.registry;

public class RequiredAttributeException extends RequiredAttributeExceptionBase
{
  private static final long serialVersionUID = -478142409;
  
  public RequiredAttributeException()
  {
    super();
  }
  
  public RequiredAttributeException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public RequiredAttributeException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public RequiredAttributeException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
