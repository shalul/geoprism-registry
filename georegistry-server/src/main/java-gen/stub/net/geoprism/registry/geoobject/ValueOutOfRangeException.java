package net.geoprism.registry.geoobject;

public class ValueOutOfRangeException extends ValueOutOfRangeExceptionBase
{
  private static final long serialVersionUID = -379848722;
  
  public ValueOutOfRangeException()
  {
    super();
  }
  
  public ValueOutOfRangeException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public ValueOutOfRangeException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public ValueOutOfRangeException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
