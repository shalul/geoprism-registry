package net.geoprism.registry;

public class ChainInheritanceException extends ChainInheritanceExceptionBase
{
  private static final long serialVersionUID = 1031883049;
  
  public ChainInheritanceException()
  {
    super();
  }
  
  public ChainInheritanceException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public ChainInheritanceException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public ChainInheritanceException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
