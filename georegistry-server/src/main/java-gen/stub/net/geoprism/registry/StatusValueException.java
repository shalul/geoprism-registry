package net.geoprism.registry;

public class StatusValueException extends StatusValueExceptionBase
{
  private static final long serialVersionUID = -1094559937;
  
  public StatusValueException()
  {
    super();
  }
  
  public StatusValueException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public StatusValueException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public StatusValueException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
