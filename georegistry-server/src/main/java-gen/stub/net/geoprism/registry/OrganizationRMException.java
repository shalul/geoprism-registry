package net.geoprism.registry;

public class OrganizationRMException extends OrganizationRMExceptionBase
{
  private static final long serialVersionUID = 1155281421;
  
  public OrganizationRMException()
  {
    super();
  }
  
  public OrganizationRMException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public OrganizationRMException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public OrganizationRMException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
