package net.geoprism.registry;

public class CGRPermissionException extends CGRPermissionExceptionBase
{
  private static final long serialVersionUID = -1042414687;
  
  public CGRPermissionException()
  {
    super();
  }
  
  public CGRPermissionException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public CGRPermissionException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public CGRPermissionException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
