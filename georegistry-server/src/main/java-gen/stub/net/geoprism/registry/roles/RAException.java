package net.geoprism.registry.roles;

public class RAException extends RAExceptionBase
{
  private static final long serialVersionUID = -2124113486;
  
  public RAException()
  {
    super();
  }
  
  public RAException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public RAException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public RAException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
