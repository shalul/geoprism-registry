package net.geoprism.registry.etl;

public class UnresolvableResourceException extends UnresolvableResourceExceptionBase
{
  private static final long serialVersionUID = -655244596;
  
  public UnresolvableResourceException()
  {
    super();
  }
  
  public UnresolvableResourceException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public UnresolvableResourceException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public UnresolvableResourceException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
