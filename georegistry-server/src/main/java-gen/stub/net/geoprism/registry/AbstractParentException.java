package net.geoprism.registry;

public class AbstractParentException extends AbstractParentExceptionBase
{
  private static final long serialVersionUID = -1375600199;
  
  public AbstractParentException()
  {
    super();
  }
  
  public AbstractParentException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public AbstractParentException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public AbstractParentException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
